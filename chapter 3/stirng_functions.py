# ==============================
# Most Used Python String Functions
# ==============================

# len()          -> Returns the length of a string
# lower()        -> Converts all characters to lowercase
# upper()        -> Converts all characters to uppercase
# title()        -> Capitalizes the first letter of each word
# capitalize()   -> Capitalizes only the first letter
# strip()        -> Removes spaces from both ends
# lstrip()       -> Removes spaces from the left side
# rstrip()       -> Removes spaces from the right side
# replace()      -> Replaces one substring with another
# find()         -> Finds the first occurrence of a substring
# count()        -> Counts occurrences of a substring
# split()        -> Splits a string into a list
# join()         -> Joins list elements into a string
# startswith()   -> Checks if a string starts with a given value
# endswith()     -> Checks if a string ends with a given value
# isalpha()      -> Returns True if all characters are letters
# isdigit()      -> Returns True if all characters are digits
# isalnum()      -> Returns True if all characters are letters or digits
# isspace()      -> Returns True if all characters are whitespace
# swapcase()     -> Swaps uppercase to lowercase and vice versa
# center()       -> Centers the string within a specified width
# zfill()        -> Pads the string with leading zeros
# format()       -> Formats values into a string
# f-string       -> Modern and easy way to format strings
# String slicing -> Extracts parts of a string (e.g., text[1:5])